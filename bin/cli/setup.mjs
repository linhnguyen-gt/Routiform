// setup — write client config so coding tools point at Routiform /v1
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { homedir } from "node:os";
import { dirname, join } from "node:path";
import { printError, printJson } from "./output.mjs";

function baseUrl(flags) {
  const port = flags.port || process.env.PORT || 20128;
  return `http://127.0.0.1:${port}/v1`;
}

function apiKey(flags) {
  return flags.apiKey || process.env.ROUTIFORM_API_KEY || "routiform";
}

function ensureDir(filePath) {
  mkdirSync(dirname(filePath), { recursive: true });
}

function writeClaude(flags) {
  // Claude Code settings.json style OpenAI-compatible base URL
  const home = homedir();
  const settingsPath = join(home, ".claude", "settings.json");
  const url = baseUrl(flags);
  const key = apiKey(flags);
  let existing = {};
  if (existsSync(settingsPath)) {
    try {
      existing = JSON.parse(readFileSync(settingsPath, "utf8"));
    } catch {
      existing = {};
    }
  }
  const env = {
    ...(existing.env || {}),
    ANTHROPIC_BASE_URL: url.replace(/\/v1$/, ""),
    ANTHROPIC_API_KEY: key,
    // Also set OpenAI-compatible vars for tools that dual-stack
    OPENAI_BASE_URL: url,
    OPENAI_API_KEY: key,
  };
  const next = { ...existing, env };
  if (flags.dryRun) {
    return { path: settingsPath, wouldWrite: next };
  }
  ensureDir(settingsPath);
  writeFileSync(settingsPath, JSON.stringify(next, null, 2) + "\n", "utf8");
  return { path: settingsPath, written: true, baseUrl: url };
}

function writeCodex(flags) {
  const home = homedir();
  const configPath = join(home, ".codex", "config.toml");
  const url = baseUrl(flags);
  const key = apiKey(flags);
  const profile = flags.profile || "routiform";
  const block = `
# Generated by: routiform setup codex
[model_providers.routiform]
name = "Routiform"
base_url = "${url}"
env_key = "ROUTIFORM_API_KEY"

[profiles.${profile}]
model_provider = "routiform"
model = "${flags.model || "auto"}"
`.trimStart();

  if (flags.dryRun) {
    return { path: configPath, wouldWrite: block, envHint: `export ROUTIFORM_API_KEY=${key}` };
  }

  ensureDir(configPath);
  let existing = existsSync(configPath) ? readFileSync(configPath, "utf8") : "";
  // Replace previous generated block if present
  const marker = "# Generated by: routiform setup codex";
  if (existing.includes(marker)) {
    existing = existing.replace(/# Generated by: routiform setup codex[\s\S]*?(?=\n# |\n\[|$)/, "");
  }
  writeFileSync(configPath, `${existing.trimEnd()}\n\n${block}\n`, "utf8");
  return {
    path: configPath,
    written: true,
    baseUrl: url,
    profile,
    envHint: `export ROUTIFORM_API_KEY=${key}`,
  };
}

export async function setupHandler(verb, args, flags) {
  const tool = verb || args[0];
  if (!tool || tool === "help") {
    console.log(`
Usage:
  routiform setup claude [--port N] [--api-key K] [--dry-run]
  routiform setup codex  [--port N] [--api-key K] [--model M] [--profile P] [--dry-run]

Writes local client config so the tool uses Routiform as OpenAI/Anthropic base URL.
`);
    return;
  }

  flags.dryRun = flags.dryRun || args.includes("--dry-run");
  for (let i = 0; i < args.length; i++) {
    if (args[i] === "--model" && args[i + 1]) flags.model = args[++i];
    if (args[i] === "--profile" && args[i + 1]) flags.profile = args[++i];
    if (args[i] === "--dry-run") flags.dryRun = true;
  }

  let result;
  if (tool === "claude" || tool === "claude-code") {
    result = writeClaude(flags);
  } else if (tool === "codex") {
    result = writeCodex(flags);
  } else {
    printError(`Unknown setup target: ${tool} (use claude|codex)`);
    process.exit(1);
  }

  if (flags.json) {
    printJson(result);
    return;
  }

  if (result.wouldWrite) {
    console.log(`Dry-run — would write ${result.path}`);
    printJson(result.wouldWrite);
    return;
  }
  console.log(`✓ Wrote ${result.path}`);
  console.log(`  base: ${result.baseUrl}`);
  if (result.envHint) console.log(`  ${result.envHint}`);
  if (result.profile) console.log(`  profile: ${result.profile}`);
}
